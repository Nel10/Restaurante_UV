    public int getCantidadSeleccionada(String tipoProducto) {
        switch (tipoProducto) {
            case "Entradas":
                return (Integer) cantEntradas.getValue();
            case "Fritos":
                return (Integer) cantFritos.getValue();
            case "Bebidas":
                return (Integer) cantBebidas.getValue();
            case "Sancocho":
                return (Integer) cantSancocho.getValue();
            case "Especiales":
                return (Integer) cantEspeciales.getValue();
            default:
                return 0; // Si no hay producto seleccionado
        }
    }

    public String getProductoSeleccionado(String tipo) {
        switch (tipo) {
            case "Entradas":
                return (String) cbxEntradas.getSelectedItem();
            case "Fritos":
                return (String) cbxFritos.getSelectedItem();
            case "Bebidas":
                return (String) cbxBebidas.getSelectedItem();
            case "Sancocho":
                return (String) cbxSancocho.getSelectedItem();
            case "Especiales":
                return (String) cbxEspeciales.getSelectedItem();
            default:
                return null;
        }
    }

