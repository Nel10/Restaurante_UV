    public void actualizarVista(Pedido pedido) {
        double total = 0.0;
        for (Producto producto : pedido.getProductos()) {
        total += producto.getPrecio(); // Asumiendo que cada producto tiene un precio
        }

        lblTotal.setText("Total: " + total);
    }



